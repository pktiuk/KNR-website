                            <p class="projects__text">
                                Łazik marsjański „HAL-062” w tym roku brał udział w University Rover Challenge. Są to odbywających się w USA zawody, na których ma
                                być symulowana współpraca pomiędzy robotem a astronautą podczas misji na marsie. Są to najbardziej prestiżowe i rozpoznawalne zawody
                                robotyczne na świecie. Robot jest wyposażony w zawieszenie typu Rocker - Bogie, napęd różnicowy oraz manipulator o 6 stopniach swobody.
