                            <p class="projects__text">
                                Łódz Podwodna OKOŃ (AUV-Autonomous Underwater Vehicle ) w pełni autonomiczny robot podwodny. Bardzo dobrą okazją do poszerzenia umiejętności w zakresie budowy jednostek
                                pływających, sterowania nimi oraz nadawania im autonomii są coroczne międzynarodowe studenckie zawody robotów podwodnych RoboSub w StanachZjednoczonych.
                                Każdy z AUV musi wykonać szereg zadań w określonym czasie, bez ingerencji operatora. Projekt obejmuje budowę w pełni wodoszczelnego
                                (do głębokości przynajmniej 4m) korpusu robota, system silników i czujników do stabilizacji poziomej i pionowej oraz szybkiego i dokładnego
                                przemieszczania się oraz chwytaka do podnoszenia i opuszczania piłeczek, a także działanie z czujnikami akustycznymi do lokalizacji celów oznaczonych dźwiękowo.
